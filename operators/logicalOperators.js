// Logical Operators
console.log(10 > 20 && 420 > 5);
// --> false --> only returns true: when both operations are true
console.log(10 > 20 || 420 > 5);
// --> true || is true, when ether one of the operations return true
console.log(10 > 20 || 420 < 5);
// --> false -- both operations are false
console.log(!(10 > 20 || 420 < 5));
